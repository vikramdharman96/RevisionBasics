package datatypes;

public class Datatypes {

	public static void main(String[] args) {
		byte pinCode = 123;
		System.out.println("Emp pin: " + pinCode);

		short Regid = 1234;
		System.out.println("Emp Regid: " + Regid);

		int i = 23432;
		System.out.println("BatchID: " + i);

		long phno = 9843117279l;
		System.out.println("Phno: " + phno);

		float sal = 23.5f;
		System.out.println("sal; " + sal);

		double average = 25.12345234235235234235252;
		System.out.println("Average: " + average);

		char gender = 'M';
		System.out.println("Gender: " + gender);
		
		boolean result = true;
		System.out.println("Gender: " + result);
		
		String mailID="ram@gamil.com";
		System.out.println("MailID: "+mailID);
	}

}
